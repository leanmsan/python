lenguajes = ["Python", "Java", "JavaScript", "R"]

#Usando for para recorrer la lista
for lenguaje in lenguajes:
    print(f"Estoy aprendiendo {lenguaje}")

#usando for
for numero in range(0, 10):
    print(numero)

#incrementos en for
for numero in range(0, 10, 2): #2 es el incremento
    print(numero)    